/**
 * Generate an ISO timestamp for N days ago at a given hour in the store timezone.
 * This ensures mock Shopify orders always align with "today" / "this week" calculations
 * using the store's timezone rather than hardcoded UTC dates.
 */
function orderDate(daysAgo: number, hour: number, minute = 0): string {
  const dateStr = daysAgoInTimezone(daysAgo);
  const h = String(hour).padStart(2, '0');
  const m = String(minute).padStart(2, '0');
  // Return as ISO-like string â€” the PnL service will convert using shopifyDateToStoreDate()
  return `${dateStr}T${h}:${m}:00Z`;
}

